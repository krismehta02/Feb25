public class A1Q3 {
    public static void main(String[] args) {
        byte Num1 = 10;
        float Num2 = 2828.28f;

        int Num3 = Num1;

        int Num4 = (int) Num2;

        System.out.println("Byte number is : " + Num1);
        System.out.println("Implicit int from byte number is : " + Num3);
        System.out.println("Float number is : " + Num2);
        System.out.println("Explicit int from float number: " + Num4);
    }
}

